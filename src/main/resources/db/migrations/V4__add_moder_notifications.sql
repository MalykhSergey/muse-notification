CREATE TYPE event_type AS ENUM (
'NEW_ANSWER_FOR_POST',
'NEW_ANSWER_FOR_YOUR_POST',
'NEW_POST_FOR_TAG',
'NEW_COMMENT_FOR_POST',
'NEW_COMMENT_FOR_YOUR_POST',
'MODERATOR_EDIT_YOUR_QUESTION',
'MODERATOR_EDIT_YOUR_ANSWER',
'MODERATOR_DELETE_YOUR_QUESTION',
'MODERATOR_DELETE_YOUR_ANSWER',
'MODERATOR_EDIT_YOUR_COMMENT',
'MODERATOR_DELETE_YOUR_COMMENT'
);

CREATE TABLE moder_notifications(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL,
    entity_id BIGINT NOT NULL,
    event_type event_type NOT NULL,
    description VARCHAR DEFAULT NULL,
    created TIMESTAMPTZ NOT NULL DEFAULT now()
);